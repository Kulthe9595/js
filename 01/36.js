// object Store key value pair 
// object don't have any index 

// how to Create Object
const person = {
    name : "shubham",
    contact : 9595838283,
};
console.log(person);

// name is key and "shubham" is value
// object contain key value pair 


// how to access data from an object
console.log(person.name);
console.log(person.contact);

// how to add key value pair to object 
person.gender = "Male";
console.log(person);


