// String Concatination

let firstName = "Shubham";
let lastName = "Kulthe";
let fullName = firstName +" "+ lastName;

console.log(fullName); //output is "Shubham Kulthe"


let String1 = "17";
let String2 = "10";

let number = String1 + String2;
console.log(number); //Output is 1710

number = +String1 + +String2;
console.log(number); //Output is 27

